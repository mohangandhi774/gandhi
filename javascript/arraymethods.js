//array methods

//push ---------------IT IS USED TO ADD A VALUE OR WORD AT LAST

let pu=["apple","banana","orange"];
pu.push("lemon");
console.log(pu);

//pop---------------IT IS USED TO REMOVE A WORD OR VALUE FROM LAST

let po=["apple","banana","orange"];
po.pop();
console.log(po);

//shift-------------IT IS USED TO REMOVE THE FIRST WORD OR VALUE

let sh=["apple","banana","orange"];
sh.shift();
console.log(sh);

//unshift-------------IT IS USED TO ADD A WORD AT FIRST

let un=["apple","banana","orange"];
un.unshift("guava");
console.log(un);

// sort --------------IT IS USED TO ARRANGE THE WORDS IN ASCENDING ORDER

let so=["or","as","not","yes"];
console.log(so.sort());

//reverse --------------IT IS USED TO REVERSE THE WHOLE WORD

let rev="ihdnag";

console.log(rev.split("").reverse("").join(""));